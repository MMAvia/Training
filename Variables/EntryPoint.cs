
using System;

namespace Variables
{
    class EntryPoint
    {
        static void Main()
        {
            //camel case naming convention
            int age = 26;
            string firstName = "MMavia";
            Console.WriteLine(age);
            Console.WriteLine(firstName);
            Console.WriteLine(age + firstName);
            Console.ReadLine();
            // 3 components of variable, Datatype, name and value

        }
    }
}
